import java.util.*;
class substring{
	public static void main (String[]args){
	str1 = "abcdefabc";
	str2 = "abc";
	int count =0;
	str1.toCharArray;
	str2.toCharArray;
	for(int i=0;i<str1.length;i++){
	for(int j=0;j<str2.length;j++){
			if (str1[i] == str2[j]){
				i++;
				j++;}
	}
		
	count++;
	}
	}
}	
